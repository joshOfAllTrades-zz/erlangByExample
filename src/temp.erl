-module(temp).
-author("jt3518").

%% API
-export([f2c/1, c2f/1, convert/1]).

convert({c, C}) ->
  {f, c2f(C)};
convert({f, F}) ->
  {c, f2c(F)}.

f2c(F) ->
  ((F - 32) / 180) * 100.

c2f(C) ->
  ((C / 100) * 180) + 32.
